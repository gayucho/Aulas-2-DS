Dia = int (input("Escolha número entre 1 e 7: "))

if(Dia == 1):
    print("domigo")
elif(Dia == 2):
    print("segunda")
elif(Dia == 3):
    print("Terça")
elif(Dia == 4):
    print("Quarta")
elif(Dia == 5):
    print("Quinta")
elif(Dia == 6):
    print("Sexta")
elif(Dia == 7):
    print("Sábado")
else:
    print("Número invalido")